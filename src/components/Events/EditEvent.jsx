// we still need useQuery eg when changing tabs without remounting the component
export function loader({ params }) {
  return queryClient.fetchQuery({
    queryKey: ['events', params.id],
    queryFn: ({ signal }) => fetchEvent({ signal, id: params.id }),
  });
}

// called when form is submitted
export async function action({ request, params }) {
  const form = await request.formData();
  const updatedEvent = Object.fromEntries(form);

  await updateEvent({ id: params.id, event: updatedEvent });
  queryClient.invalidateQueries(['events', params.id]); // no more optimistic update here

  return redirect('../');
}